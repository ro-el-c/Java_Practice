public class StringPractice {
    public static void main(String[] args) {
        String noneString = "";
        String withBlank = " ";

        System.out.println("noneString.isEmpty() = " + noneString.isEmpty());
        System.out.println("withBlank.isBlank() = " + noneString.isBlank());

        System.out.println("withBlank.isEmpty() = " + withBlank.isEmpty());
        System.out.println("withBlank.isBlank() = " + withBlank.isBlank());

    }
}
